# Assignment Operators
x = 5
x += 3  # x = x + 3
print("After +=:", x)  # 8

x -= 2  # x = x - 2
print("After -=:", x)  # 6

x *= 2  # x = x * 2
print("After *=:", x)  # 12

x /= 3  # x = x / 3
print("After /=:", x)  # 4.0

x //= 2  # x = x // 2
print("After //=:", x) # 2

x %= 2  # x = x % 2
print("After %=:", x)  # 0

x = 2
x **= 3  # x = x ** 3
print("After **=:", x) 
